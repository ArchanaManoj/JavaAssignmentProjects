package comm.assnmnt.day1;

public class DataPrimitive {
	public static void main(String[] args) {
		short s1 = 32;
	    System.out.println("A short: " +  s1);
	    s1 = 365;
	    System.out.println("A short: " +  s1);
	    s1 = (short)35000;
	    System.out.println("A short: " +  s1);
	    s1 = (short)65398;
	    System.out.println("A short: " +  s1);
	    
	    int s2 = 365;
	    s2 = 35000;
	    System.out.println("An int: " +  s2);
	    s2 = 65398;
	    System.out.println("An int: " +  s2);
	    s2 = 1212673984;
	    System.out.println("An int: " +  s2);
	    s2 = 1212673984;
	    System.out.println("An int: " +  s2);
//	    long s3 = 12345678901234567890123L;
	    long s3 = 1234567890123456789L;
	    System.out.println("A short: " +  s3);
	    float s4 = 123.456f;
	    System.out.println("A float: " +  s4);
	    s4 = (float) 34.5;
	    System.out.println("A float: " +  s4);
	    double s5 = 234.56;
	    System.out.println("A double: " +  s5);
	    char ch1= 'A';
	    System.out.println("A character: " +  ch1);
//	    char ch2= 'AA';
	    char ch3 = '"';
	    System.out.println("A character: " +  ch3);
	}

}
